/**
 * op_add - adds two integers
 * @a: first operand
 * @b: second operand
 *
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the differnce of two integers
 * @a: first operand
 * @b: second operand
 *
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns product of two integers
 * @a: first operand
 * @b: second operand
 *
 * Return: multiplication of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the result of the division of two integers
 * @a: first operand
 * @b: second operand
 *
 * Return: result of division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of the division two integers
 * @a: first operand
 * @b: second operand
 *
 * Return: modulus of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}

